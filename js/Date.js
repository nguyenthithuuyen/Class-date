let MyDate = function(day, month, year) {

    this.day = day;

    this.month = month;

    this.year = year;
}
    this.setDay = function (days) {
        day = days;
    };
    this.getDay = function () {
        return day
    };
    this.getMonth = function () {
        return month;
    };
    this.getYear = function () {
        return year
    };
